        private string RunBytes(byte[] data)
        {
            var waiter = new ManualResetEventSlim(false);

            var xml = "";
            _service.ActiveCommand = 0x00;
            var handler = _service.Handler;
            handler.GridUpdated += (sender, args) =>
            {
                xml = args.Data.ToString();
                waiter.Set();
            };

            _service.ProcessOutbound(data, 0, 0);
            handler.Update();

            waiter.Wait();
            return xml;
        }

